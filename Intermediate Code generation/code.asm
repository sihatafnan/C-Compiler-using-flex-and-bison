.MODEL SMALL
.STACK 100H
.DATA
	a1 DW ?
	b1 DW ?
	c1 DW 3 DUP(?)
	T1 DW ?
	T2 DW ?
	T3 DW ?
	T4 DW ?
	T5 DW ?
.CODE
PRINT_ID PROC

	;SAVE IN STACK
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX

	;CHECK IF NEGATIVE
	OR AX, AX
	JGE PRINT_NUMBER

	;PRINT MINUS SIGN
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX

	NEG AX

	PRINT_NUMBER:
	XOR CX, CX
	MOV BX, 10D

	REPEAT_CALC:

		;AX:DX- QUOTIENT:REMAINDER
		XOR DX, DX
		DIV BX  ;DIVIDE BY 10
		PUSH DX ;PUSH THE REMAINDER IN STACK

		INC CX

		OR AX, AX
		JNZ REPEAT_CALC

	MOV AH, 2

	PRINT_LOOP:
		POP DX
		ADD DL, 30H
		INT 21H
		LOOP PRINT_LOOP

	;NEWLINE
	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H

	POP AX
	POP BX
	POP CX
	POP DX

	RET
PRINT_ID ENDP

MAIN PROC

	;INITIALIZE DATA SEGMENT
	MOV AX, @DATA
	MOV DS, AX


	MOV AX, 2
	ADD AX, 3
	MOV T1, AX

	MOV AX, 1
	MOV BX, T1
	MUL BX
	MOV T2, AX

	MOV AX, T2
	MOV BX, 3
	XOR DX, DX
	DIV BX
	MOV T3 , DX

	MOV AX, T3
	MOV a1, AX

	MOV AX, 1
	CMP AX, 5
	JL L1

	MOV T4, 0
	JMP L2

	L1:
	MOV T4, 1

	L2:

	MOV AX, T4
	MOV b1, AX

	MOV AX, 2
	MOV c1, AX

	MOV AX, a1
	MOV BX, b1
	CMP AX, 1
	JNE L3
	CMP BX, 1
	JNE L3
	MOV AX, 1
	MOV T5, AX
	JMP L4

	L3:
	MOV AX, 0
	MOV T5, AX

	L4:
	MOV AX, T5
	CMP AX, 0
	JE L5
	MOV AX, c1
	INC AX
	MOV c1, AX
	JMP L6
	L5:

	MOV AX, c1
	MOV c1+1*2, AX

	L6:

	MOV AX, a1
	CALL PRINT_ID

	MOV AX, b1
	CALL PRINT_ID

	MOV AX, 4CH
	INT 21H
MAIN ENDP

END MAIN
